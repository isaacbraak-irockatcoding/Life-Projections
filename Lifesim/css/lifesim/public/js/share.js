function renderShareTab() {
    if (!ST.user) return;
    document.getElementById('share-avatar').textContent = ST.user.avatar;
    document.getElementById('share-name').textContent   = ST.user.name;
    document.getElementById('share-meta').textContent   =
      `Age ${ST.user.age} · ${ST.scenarios.length} scenario${ST.scenarios.length > 1 ? 's' : ''}`;
  
    const visible = ST.showC ? ST.scenarios : ST.scenarios.slice(0, 2);
    const results = visible.map((s, i) => calculatePath(s, i));
    document.getElementById('share-paths-preview').innerHTML = visible.map((s, i) => {
      const final = results[i].path[results[i].path.length - 1];
      const job   = JOBS.find(j => j.id === s.jobId)?.name || s.jobId;
      return `<div style="margin-bottom:14px;">
        <div style="font-family:var(--font-num);font-size:22px;font-weight:600;color:${s.color};">${fmtM(final)}</div>
        <div style="font-size:12px;color:var(--muted2);margin-top:3px;">${s.label} · ${job} · Retire @ ${s.retireAge}</div>
      </div>`;
    }).join('');
  }
  
  async function copySynopsis() {
    const visible = ST.showC ? ST.scenarios : ST.scenarios.slice(0, 2);
    let txt = `📡 LifeSim — ${ST.user?.name || 'My'} Financial Projections\n\n`;
    visible.forEach((s, i) => {
      const res   = calculatePath(s, i);
      const final = res.path[res.path.length - 1];
      const job   = JOBS.find(j => j.id === s.jobId)?.name || s.jobId;
      txt += `${s.label} (${job})\n`;
      txt += `  Assets: $${s.currentAssets.toLocaleString()} | Debt: $${s.currentDebt.toLocaleString()}\n`;
      txt += `  Savings: ${s.savePct}% | Return: ${s.returnRate}% | Retire: ${s.retireAge}\n`;
      txt += `  Est. Net Worth @ ${ST.startAge + 45}: ${fmtM(final)}\n\n`;
    });
    txt += `⚠️ For informational purposes only. Not financial advice.\nGenerated by LifeSim.`;
  
    try {
      await navigator.clipboard.writeText(txt);
      alert('Copied to clipboard! 📋');
    } catch {
      const ta = Object.assign(document.createElement('textarea'),
        { value: txt, style: 'position:fixed;opacity:0;' });
      document.body.appendChild(ta); ta.select();
      document.execCommand('copy'); ta.remove();
      alert('Copied to clipboard! 📋');
    }
  }
  
  function exportChart(canvasId, filename) {
    const canvas = document.getElementById(canvasId);
    if (!canvas) return;
    Object.assign(document.createElement('a'),
      { download: filename + '.png', href: canvas.toDataURL('image/png') }
    ).click();
  }
  
  function resetProfile() {
    if (!confirm('Reset your profile and all data? This cannot be undone.')) return;
    localStorage.clear();
    location.reload();
  }